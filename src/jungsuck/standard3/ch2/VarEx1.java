package jungsuck.standard3.ch2;

public class VarEx1 {
    public static void main(String[] args) {
        int year = 0;
        int age = 14;

        System.out.println(year);
        System.out.println(age);

        year = age + 2000; // 2014
        age = age + 1; // 15

        System.out.println(year);
        System.out.println(age);
    }
}
